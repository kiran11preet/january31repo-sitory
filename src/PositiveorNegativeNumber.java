public class PositiveorNegativeNumber {
    public static void main(String[] args) {

        int x = 50;
        if (x > 0) {

            System.out.println("X is a positive number");
        }
        else if(x < 0) {
            System.out.println("X is a negative number");
        }
        else{
            System.out.println("X is equal to zero");
        }
    }
}
